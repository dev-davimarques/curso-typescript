function somar (a: number, b: number): number{
    return a + b;
}
let soma: number = somar(2, 3);
console.log("Função somar, resultado é: "+soma) // Saída: 5

function subtrair (a: number, b: number): number{
    return a - b;
}
let subtracao: number = subtrair(5, 9);
console.log("Função subtrair, resultado é: "+subtracao)